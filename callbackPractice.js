var each = function(array, callback){
  for (i = 0; i < array.length; i++){
    var itm = array[i];
    var index = i;
    callback(itm, index)
    }
  }


var names = ['Tyler', 'Cahlan', 'Ryan', 'Colt', 'Tyler', 'Blaine', 'Cahlan'];
